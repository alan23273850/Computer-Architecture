#ifndef DISK_H
#define DISK_H

typedef struct disk {
    int sizeP;
    struct {
        unsigned char byte[1024];
    } page[256];

    /* a function to convert absolution address into page-byte index */
    /* note we return the "reference type (&)" here */
    unsigned char& byte(int b) {
        return page[b/sizeP].byte[b%sizeP];
    }
} DISK;

#endif // DISK_H
